// ── Save ──

void EnvironmentKcm::save()
{
    // 1. System vars: write /etc/environment via KAuth
    {
        const QMap<QString, QString> sysMap = varListToMap(m_systemVars);

        QString content = QStringLiteral("# Generated by NSA Fedora System Manager\n");
        for (auto it = sysMap.constBegin(); it != sysMap.constEnd(); ++it) {
            const QString &val = it.value();
            if (val.contains(QLatin1Char(' ')) || val.contains(QLatin1Char('\t'))) {
                content += it.key() + QStringLiteral("=\"") + val + QStringLiteral("\"\n");
            } else {
                content += it.key() + QLatin1Char('=') + val + QLatin1Char('\n');
            }
        }

        QVariantMap args;
        args[QStringLiteral("filePath")] = QStringLiteral("/etc/environment");
        args[QStringLiteral("content")] = content.toUtf8();

        KAuth::Action action(QStringLiteral("org.kde.nsafsm.write"));
        action.setHelperId(QStringLiteral("org.kde.nsafsm"));
        action.setArguments(args);

        KAuth::ExecuteJob *job = action.execute();
        connect(job, &KJob::result, this, [this, job] {
            if (job->error()) {
                qWarning() << "KAuth write failed (system env):" << job->errorString();
            }
        });
        job->start();
    }

    // 2. User vars: write directly to ~/.config/environment.d/nsa-fsm.conf
    {
        const QString userPath = userConfPath();
        const QDir dir = QFileInfo(userPath).absoluteDir();
        if (!dir.exists()) {
            dir.mkpath(QStringLiteral("."));
        }

        const QMap<QString, QString> userMap = varListToMap(m_userVars);
        NsaFsm::ConfigParser::writeKeyValue(userPath, userMap);
    }

    setNeedsSave(false);
}

